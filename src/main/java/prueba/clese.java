package prueba;

public class clese {
    private String nombre;
    private int edad;

    public clese(String nombre, int edad) {
        this.edad = edad;
        this.nombre = nombre;

    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

}
